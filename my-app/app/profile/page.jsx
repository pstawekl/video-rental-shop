'use client';
import React, { useEffect, useState } from 'react';
import { Row, Col } from 'reactstrap';
import { useUser, withPageAuthRequired } from '@auth0/nextjs-auth0/client';
import Loading from '../../components/Loading';
import ErrorMessage from '../../components/ErrorMessage';
import Highlight from '../../components/Highlight';
import { Button, Form, FormGroup, Label, Input, FormText } from 'reactstrap';


function Profile() {
  const { user, isLoading } = useUser();

  return (
    <>
      {isLoading && <Loading />}
      {user && (
        <>
          <Row className="align-items-center profile-header mb-5 text-center text-md-left" data-testid="profile">
            <Col md={2}>
              <img
                src={user.picture}
                alt="Profile"
                className="rounded-circle img-fluid profile-picture mb-3 mb-md-0"
                decode="async"
                data-testid="profile-picture"
              />
            </Col>
            <Col md>
              <h2 data-testid="profile-name">{user.name}</h2>
              <p className="lead text-muted" data-testid="profile-email">
                {user.email}
              </p>
            </Col>
          </Row>
          <UserForm user={user} />

          {/* <Row data-testid="profile-json">
            <Highlight>{JSON.stringify(user, null, 2)}</Highlight>
          </Row> */}
        </>
      )}
    </>
  );
}

export default withPageAuthRequired(Profile, {
  onRedirecting: () => <Loading />,
  onError: error => <ErrorMessage>{error.message}</ErrorMessage>
});

function UserForm({ user }) {
  // Stan formularza
  const [formData, setFormData] = useState({
    user_country: '',
    user_city: '',
    user_street: '',
    user_house_number: '',
    user_postcode: '',
    user_phone: '',
    user_phone_country: ''
  });
  const [userInfo, setUserInfo] = useState();

  useEffect(() => {
    fetch('/api/mssql/get-user-info', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ user: user })
    }).then(response => response.json())
    .then(data => {
      console.log('userInfo', data.userInfo);
      setFormData({
        user_city: data.userInfo.user_city,
        user_country: data.userInfo.user_country,
        user_house_number: data.userInfo.user_house_number,
        user_phone: data.userInfo.user_phone,
        user_phone_country: data.userInfo.user_phone_country,
        user_postcode: data.userInfo.user_postcode,
        user_street: data.userInfo.user_street
      })
    })
  }, [])

  // Aktualizacja stanu przy zmianie wartości w formularzu
  const handleChange = (e) => {
    setFormData({
      ...formData,
      [e.target.name]: e.target.value
    });
  };

  // Obsługa wysyłania formularza
  const handleSubmit = (e) => {
    e.preventDefault();
    // Wysyłanie danych do serwera
    fetch('/api/mssql/user-update', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ formData: formData, user: user })
    })
      .then(response => response.json())
      .then(data => console.log('Success:', data))
      .catch(error => console.error('Error:', error));
  };

  return (
    <Form onSubmit={handleSubmit}>
      <FormGroup>
        <Label for="user_country">Państwo</Label>
        <Input type="select" name="user_country" id="user_country" value={formData.user_country} onChange={handleChange}>
          {Object.entries(countryList[0]).map(([key, value]) => (
            <option key={key} value={key}>{value}</option>
          ))}
        </Input>
      </FormGroup>
      <FormGroup>
        <Label for="user_city">Miasto</Label>
        <Input type="text" name="user_city" id="user_city" value={formData.user_city} onChange={handleChange} />
      </FormGroup>
      <FormGroup>
        <Label for="user_street">Ulica</Label>
        <Input type="text" name="user_street" id="user_street" value={formData.user_street} onChange={handleChange} />
      </FormGroup>
      <FormGroup>
        <Label for="user_house_number">Numer domu/mieszkania</Label>
        <Input type="text" name="user_house_number" id="user_house_number" value={formData.user_house_number} onChange={handleChange} />
      </FormGroup>
      <FormGroup>
        <Label for="user_postcode">Kod pocztowy</Label>
        <Input type="text" name="user_postcode" id="user_postcode" value={formData.user_postcode} onChange={handleChange} />
      </FormGroup>
      <FormGroup>
        <Label for="user_phone">Telefon</Label>
        <Input type="text" name="user_phone" id="user_phone" value={formData.user_phone} onChange={handleChange} />
      </FormGroup>
      {/* Powtórz powyższy wzorzec dla każdego pola formularza */}
      {/* ... */}
      <Button type="submit">Submit</Button>
    </Form>
  );
}

